type rpcMethod struct {
	name           string
	fullMethodName string
	requestType    string
	responseType   string
}

func main() {
	repoRoot, err := os.Getwd()
	if err != nil {
		panic(err)
	}

	var b strings.Builder
	b.WriteString("// Code generated by scripts/generate_intercepted_services; DO NOT EDIT.\n\n")
	b.WriteString("package grpc\n\n")
	b.WriteString("import (\n\t\"context\"\n\n")

	imports := map[string]string{}
	for _, svc := range services {
		imports[svc.importName] = svc.importPath
	}
	importNames := make([]string, 0, len(imports))
	for name := range imports {
		importNames = append(importNames, name)
	}
	sort.Strings(importNames)
	for _, name := range importNames {
		b.WriteString(fmt.Sprintf("\t%s %q\n", name, imports[name]))
	}
	b.WriteString(")\n\n")

	for _, svc := range services {
		methods, err := parseServiceMethods(filepath.Join(repoRoot, "pkg/protos", svc.protoDir, svc.protoDir+"_grpc.pb.go"), svc.serverName)
		if err != nil {
			panic(fmt.Sprintf("%s: %v", svc.protoDir, err))
		}

		b.WriteString(fmt.Sprintf("func %s(inner %s.%s, chain *UnaryChain) %s.%s {\n", svc.wrapFunc, svc.importName, svc.serverName, svc.importName, svc.serverName))
		b.WriteString(fmt.Sprintf("\tif chain == nil {\n\t\treturn inner\n\t}\n"))
		b.WriteString(fmt.Sprintf("\treturn &%s{inner: inner, chain: chain}\n", svc.structName))
		b.WriteString("}\n\n")

		b.WriteString(fmt.Sprintf("type %s struct {\n\t%s.Unimplemented%s\n\tinner %s.%s\n\tchain *UnaryChain\n}\n\n", svc.structName, svc.importName, svc.serverName, svc.importName, svc.serverName))

		for _, method := range methods {
			b.WriteString(fmt.Sprintf("func (s *%s) %s(ctx context.Context, req *%s.%s) (*%s.%s, error) {\n", svc.structName, method.name, svc.importName, method.requestType, svc.importName, method.responseType))
			b.WriteString(fmt.Sprintf("\tresp, err := s.chain.Invoke(ctx, %s.%s, req, func(ctx context.Context, req any) (any, error) {\n", svc.importName, method.fullMethodName))
			b.WriteString(fmt.Sprintf("\t\treturn s.inner.%s(ctx, req.(*%s.%s))\n", method.name, svc.importName, method.requestType))
			b.WriteString("\t})\n")
			b.WriteString("\tif err != nil {\n\t\treturn nil, err\n\t}\n")
			b.WriteString(fmt.Sprintf("\treturn resp.(*%s.%s), nil\n", svc.importName, method.responseType))
			b.WriteString("}\n\n")
		}
	}

	formatted, err := format.Source([]byte(b.String()))
	if err != nil {
		fmt.Fprintf(os.Stderr, "format error: %v\n", err)
		os.WriteFile("pkg/grpc/intercepted_services_gen.go", []byte(b.String()), 0o644)
		panic(err)
	}

	outPath := filepath.Join(repoRoot, "pkg/grpc/intercepted_services_gen.go")
	if err := os.WriteFile(outPath, formatted, 0o644); err != nil {
		panic(err)
	}

	fmt.Printf("generated %s\n", outPath)
}

func parseServiceMethods(path, serverName string) ([]rpcMethod, error) {
	content, err := os.ReadFile(path)
	if err != nil {
		return nil, err
	}

	methodPattern := regexp.MustCompile(`(?m)^\s*([A-Z][A-Za-z0-9]+)\(context\.Context, \*([A-Za-z0-9]+)\) \(\*([A-Za-z0-9]+), error\)`)
	constPattern := regexp.MustCompile(`(?m)^\s*([A-Za-z0-9_]+_FullMethodName)\s*=`)

	fullMethods := map[string]string{}
	for _, match := range constPattern.FindAllStringSubmatch(string(content), -1) {
		constName := match[1]
		prefix := strings.TrimSuffix(constName, "_FullMethodName")
		idx := strings.Index(prefix, "_")
		if idx < 0 {
			continue
		}
		fullMethods[prefix[idx+1:]] = constName
	}

	interfacePattern := regexp.MustCompile(`(?s)type ` + serverName + ` interface \{(.+?)\}`)
	interfaceMatch := interfacePattern.FindStringSubmatch(string(content))
	if len(interfaceMatch) != 2 {
		return nil, fmt.Errorf("server interface %s not found", serverName)
	}

	var methods []rpcMethod
	for _, match := range methodPattern.FindAllStringSubmatch(interfaceMatch[1], -1) {
		methods = append(methods, rpcMethod{
			name:         match[1],
			requestType:  match[2],
			responseType: match[3],
		})
	}

	for i := range methods {
		constName, ok := fullMethods[methods[i].name]
		if !ok {
			return nil, fmt.Errorf("missing FullMethodName for %s", methods[i].name)
		}
		methods[i].fullMethodName = constName
	}

	return methods, nil
}
