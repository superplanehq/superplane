func (c *DownloadArtifact) Setup(ctx core.SetupContext) error {
	spec := DownloadArtifactSpec{}
	if err := mapstructure.Decode(ctx.Configuration, &spec); err != nil {
		return fmt.Errorf("failed to decode configuration: %w", err)
	}

	if spec.Agent == "" {
		return fmt.Errorf("agent is required")
	}

	if spec.Path == "" {
		return fmt.Errorf("artifact path is required")
	}

	return nil
}

func (c *DownloadArtifact) ProcessQueueItem(ctx core.ProcessQueueContext) (*uuid.UUID, error) {
	return ctx.DefaultProcessing()
}

func (c *DownloadArtifact) Execute(ctx core.ExecutionContext) error {
	spec := DownloadArtifactSpec{}
	if err := mapstructure.Decode(ctx.Configuration, &spec); err != nil {
		return fmt.Errorf("failed to decode configuration: %w", err)
	}

	if spec.Agent == "" {
		return fmt.Errorf("agent is required")
	}

	if spec.Path == "" {
		return fmt.Errorf("artifact path is required")
	}

	client, err := NewClient(ctx.HTTP, ctx.Integration)
	if err != nil {
		return fmt.Errorf("failed to create cursor client: %w", err)
	}

	if client.LaunchAgentKey == "" {
		return fmt.Errorf("cloud agent API key is not configured in the integration")
	}

	ctx.Logger.Infof("Downloading artifact %s from Cursor agent %s", spec.Path, spec.Agent)

	response, err := client.DownloadArtifact(spec.Agent, spec.Path)
	if err != nil {
		return fmt.Errorf("failed to download artifact: %w", err)
	}

	output := DownloadArtifactOutput{
		AgentID:   spec.Agent,
		Path:      spec.Path,
		URL:       response.URL,
		ExpiresAt: response.ExpiresAt,
	}

	return ctx.ExecutionState.Emit(core.DefaultOutputChannel.Name, DownloadArtifactPayloadType, []any{output})
}

