const (
	DownloadArtifactPayloadType = "cursor.downloadArtifact.result"
)

type DownloadArtifact struct{}

type DownloadArtifactSpec struct {
	Agent string `json:"agent" mapstructure:"agent"`
	Path  string `json:"path" mapstructure:"path"`
}

type DownloadArtifactOutput struct {
	AgentID   string `json:"agentId"`
	Path      string `json:"path"`
	URL       string `json:"url"`
	ExpiresAt string `json:"expiresAt"`
}

func (c *DownloadArtifact) Name() string {
	return "cursor.downloadArtifact"
}

func (c *DownloadArtifact) Label() string {
	return "Download Artifact"
}

func (c *DownloadArtifact) Description() string {
	return "Retrieves a temporary download URL for an artifact produced by a Cursor Cloud Agent."
}

func (c *DownloadArtifact) Documentation() string {
	return `The Download Artifact component retrieves a temporary presigned URL for an artifact produced by a Cursor Cloud Agent.

## Use Cases

- **Retrieve agent outputs**: Download screenshots, reports, or other files generated by a Cloud Agent
- **Pass artifacts downstream**: Feed artifact URLs to later workflow steps for processing or publishing
- **Archive results**: Store agent-produced files in external storage systems

## How It Works

1. Calls Cursor's Cloud Agents API to request a download URL for the specified artifact
2. Emits the presigned URL together with its expiration time

## Configuration

- **Agent**: The Cloud Agent that produced the artifact
- **Artifact Path**: The artifact to download, selected from the agent's artifacts or provided as an expression (for example, ` + "`artifacts/screenshot.png`" + `)

## Output

The output includes:
- **agentId**: The agent the artifact belongs to
- **path**: The artifact path that was requested
- **url**: A presigned URL to download the artifact
- **expiresAt**: When the presigned URL expires

## Notes

- Requires a valid Cursor Cloud Agent API key configured in the integration
- The presigned URL is valid for 15 minutes
- Artifacts must live under the agent workspace's ` + "`artifacts/`" + ` directory`
}

