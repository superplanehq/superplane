const ContainerFileDownloadedPayloadType = "openai.containerFile.downloaded"

type DownloadContainerFile struct{}

type DownloadContainerFileSpec struct {
	ContainerID string `json:"containerId" mapstructure:"containerId"`
	FileID      string `json:"fileId" mapstructure:"fileId"`
}

// ContainerFilePayload carries the downloaded container file content: text
// files as a plain string, everything else base64-encoded.
type ContainerFilePayload struct {
	FileID      string `json:"fileId"`
	ContainerID string `json:"containerId"`
	Path        string `json:"path"`
	Filename    string `json:"filename"`
	Bytes       int64  `json:"bytes"`
	Encoding    string `json:"encoding"`
	Content     string `json:"content"`
}

func (c *DownloadContainerFile) Name() string {
	return "openai.downloadContainerFile"
}

func (c *DownloadContainerFile) Label() string {
	return "Download Container File"
}

func (c *DownloadContainerFile) Description() string {
	return "Download a file from a code interpreter container"
}

func (c *DownloadContainerFile) Documentation() string {
	return `The Download Container File component downloads a file from a code interpreter container,
such as the artifacts generated by a Text Prompt with Code Interpreter enabled.

## Use Cases

- **Retrieve artifacts**: Download charts, spreadsheets, or other files the model generated
- **Workflow data**: Feed generated file content into downstream nodes for processing
- **Archiving**: Push generated files to external storage systems

## Configuration

- **Container ID**: The container holding the file, typically an expression referencing a Text Prompt artifact
- **File ID**: The container file to download, typically an expression referencing a Text Prompt artifact

## Output

Returns the file content and metadata:
- **fileId**: The container file ID
- **containerId**: The container ID
- **path**: The file path inside the container (e.g. /mnt/data/plot.png)
- **filename**: The file name, derived from the path
- **bytes**: The file size in bytes
- **encoding**: "text" for text content, "base64" for binary content
- **content**: The file content, base64-encoded when binary

## Notes

- Files larger than 25MB are rejected; the size is checked before downloading
- Containers expire about 20 minutes after their last activity, so download files promptly`
}

