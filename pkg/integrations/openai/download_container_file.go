func (c *DownloadContainerFile) Setup(ctx core.SetupContext) error {
	spec := DownloadContainerFileSpec{}
	if err := mapstructure.Decode(ctx.Configuration, &spec); err != nil {
		return fmt.Errorf("failed to decode configuration: %v", err)
	}

	if strings.TrimSpace(spec.ContainerID) == "" {
		return errors.New("containerId is required")
	}

	if strings.TrimSpace(spec.FileID) == "" {
		return errors.New("fileId is required")
	}

	return nil
}

func (c *DownloadContainerFile) Execute(ctx core.ExecutionContext) error {
	spec := DownloadContainerFileSpec{}
	if err := mapstructure.Decode(ctx.Configuration, &spec); err != nil {
		return fmt.Errorf("failed to decode configuration: %v", err)
	}

	containerID := strings.TrimSpace(spec.ContainerID)
	if containerID == "" {
		return errors.New("containerId is required")
	}

	fileID := strings.TrimSpace(spec.FileID)
	if fileID == "" {
		return errors.New("fileId is required")
	}

	client, err := NewClient(ctx.HTTP, ctx.Integration)
	if err != nil {
		return err
	}

	file, err := client.GetContainerFile(containerID, fileID)
	if err != nil {
		return fmt.Errorf("failed to get container file: %w", err)
	}

	if file.Bytes > maxDownloadBytes {
		return fmt.Errorf("file is %d bytes, which exceeds the %dMB download limit", file.Bytes, maxDownloadBytes/(1024*1024))
	}

	content, err := client.DownloadContainerFileContent(containerID, fileID)
	if err != nil {
		return fmt.Errorf("failed to download container file content: %w", err)
	}

	encoding, encoded := encodeFileContent(content)

	return ctx.ExecutionState.Emit(
		core.DefaultOutputChannel.Name,
		ContainerFileDownloadedPayloadType,
		[]any{ContainerFilePayload{
			FileID:      fileID,
			ContainerID: containerID,
			Path:        file.Path,
			Filename:    containerFileName(file.Path),
			Bytes:       file.Bytes,
			Encoding:    encoding,
			Content:     encoded,
		}},
	)
}

// containerFileName derives the file name from the container path
// (e.g. /mnt/data/plot.png -> plot.png).
func containerFileName(containerPath string) string {
	if containerPath == "" {
		return ""
	}
	return path.Base(containerPath)
}

