const (
	FileDownloadedPayloadType = "claude.file.downloaded"
	// maxDownloadSizeBytes caps how large a file can be emitted as payload data.
	maxDownloadSizeBytes = 25 * 1024 * 1024
)

type DownloadFile struct{}

type DownloadFileSpec struct {
	File string `json:"file"`
}

// FileContentPayload is emitted with the file's content, either as plain text
// or base64-encoded depending on the file's MIME type.
type FileContentPayload struct {
	ID        string `json:"id"`
	Filename  string `json:"filename"`
	MimeType  string `json:"mimeType"`
	SizeBytes int64  `json:"sizeBytes"`
	Encoding  string `json:"encoding"`
	Content   string `json:"content"`
}

