func (c *DownloadFile) Setup(ctx core.SetupContext) error {
	spec := DownloadFileSpec{}
	if err := mapstructure.Decode(ctx.Configuration, &spec); err != nil {
		return fmt.Errorf("failed to decode configuration: %v", err)
	}

	fileID := strings.TrimSpace(spec.File)
	if fileID == "" {
		return errors.New("file is required")
	}

	return resolveFileMetadata(ctx, fileID)
}

func (c *DownloadFile) Execute(ctx core.ExecutionContext) error {
	spec := DownloadFileSpec{}
	if err := mapstructure.Decode(ctx.Configuration, &spec); err != nil {
		return fmt.Errorf("failed to decode configuration: %v", err)
	}

	fileID := strings.TrimSpace(spec.File)
	if fileID == "" {
		return errors.New("file is required")
	}

	client, err := NewClient(ctx.HTTP, ctx.Integration)
	if err != nil {
		return err
	}

	// Check the size before downloading so oversized files are rejected
	// without transferring their content.
	file, err := client.GetFileMetadata(fileID)
	if err != nil {
		return fmt.Errorf("failed to get file: %w", err)
	}
	if file.SizeBytes > maxDownloadSizeBytes {
		return fmt.Errorf("file %s is %d bytes, which exceeds the %d byte download limit", fileID, file.SizeBytes, maxDownloadSizeBytes)
	}

	content, err := client.DownloadFile(fileID)
	if err != nil {
		return fmt.Errorf("failed to download file: %w", err)
	}

	payload := FileContentPayload{
		ID:        file.ID,
		Filename:  file.Filename,
		MimeType:  file.MimeType,
		SizeBytes: file.SizeBytes,
	}
	if isTextMIME(file.MimeType) {
		payload.Encoding = "text"
		payload.Content = string(content)
	} else {
		payload.Encoding = "base64"
		payload.Content = base64.StdEncoding.EncodeToString(content)
	}

	return ctx.ExecutionState.Emit(
		core.DefaultOutputChannel.Name,
		FileDownloadedPayloadType,
		[]any{payload},
	)
}

// isTextMIME reports whether content of the given MIME type is safe to emit as
// plain text; anything else is base64-encoded.
func isTextMIME(mimeType string) bool {
	mimeType = strings.ToLower(strings.TrimSpace(mimeType))
	if i := strings.Index(mimeType, ";"); i >= 0 {
		mimeType = strings.TrimSpace(mimeType[:i])
	}
	if strings.HasPrefix(mimeType, "text/") {
		return true
	}
	if strings.HasSuffix(mimeType, "+json") || strings.HasSuffix(mimeType, "+xml") {
		return true
	}
	switch mimeType {
	case "application/json",
		"application/xml",
		"application/x-yaml",
		"application/yaml",
		"application/javascript",
		"application/x-sh",
		"application/sql",
		"application/csv":
		return true
	}
	return false
}

