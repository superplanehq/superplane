const FileFetchedPayloadType = "claude.file.fetched"

type GetFile struct{}

type GetFileSpec struct {
	File string `json:"file"`
}

// FilePayload is emitted with the file's metadata and download link.
type FilePayload struct {
	ID           string `json:"id"`
	Filename     string `json:"filename"`
	MimeType     string `json:"mimeType"`
	SizeBytes    int64  `json:"sizeBytes"`
	CreatedAt    string `json:"createdAt"`
	Downloadable bool   `json:"downloadable"`
	DownloadURL  string `json:"downloadUrl"`
}

// FileNodeMetadata stores a display label for file integration resources
// (picker value is the file ID).
type FileNodeMetadata struct {
	Filename string `json:"filename"`
}

