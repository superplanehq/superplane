func (c *GetFile) Setup(ctx core.SetupContext) error {
	spec := GetFileSpec{}
	if err := mapstructure.Decode(ctx.Configuration, &spec); err != nil {
		return fmt.Errorf("failed to decode configuration: %v", err)
	}

	fileID := strings.TrimSpace(spec.File)
	if fileID == "" {
		return errors.New("file is required")
	}

	return resolveFileMetadata(ctx, fileID)
}

// resolveFileMetadata best-effort resolves the configured file's name into node
// metadata so the UI can show a label instead of the raw file ID. Expressions
// and lookup failures fall back to the ID; they never fail Setup.
func resolveFileMetadata(ctx core.SetupContext, fileID string) error {
	if ctx.Metadata == nil || ctx.Integration == nil || ctx.HTTP == nil {
		return nil
	}

	meta := FileNodeMetadata{Filename: fileID}
	if !strings.Contains(fileID, "{{") {
		if client, err := NewClient(ctx.HTTP, ctx.Integration); err == nil {
			if file, err := client.GetFileMetadata(fileID); err == nil && file.Filename != "" {
				meta.Filename = file.Filename
			}
		}
	}

	return ctx.Metadata.Set(meta)
}

func (c *GetFile) Execute(ctx core.ExecutionContext) error {
	spec := GetFileSpec{}
	if err := mapstructure.Decode(ctx.Configuration, &spec); err != nil {
		return fmt.Errorf("failed to decode configuration: %v", err)
	}

	fileID := strings.TrimSpace(spec.File)
	if fileID == "" {
		return errors.New("file is required")
	}

	client, err := NewClient(ctx.HTTP, ctx.Integration)
	if err != nil {
		return err
	}

	file, err := client.GetFileMetadata(fileID)
	if err != nil {
		return fmt.Errorf("failed to get file: %w", err)
	}

	payload := FilePayload{
		ID:           file.ID,
		Filename:     file.Filename,
		MimeType:     file.MimeType,
		SizeBytes:    file.SizeBytes,
		CreatedAt:    file.CreatedAt,
		Downloadable: file.Downloadable,
		DownloadURL:  client.FileContentURL(file.ID),
	}

	return ctx.ExecutionState.Emit(
		core.DefaultOutputChannel.Name,
		FileFetchedPayloadType,
		[]any{payload},
	)
}

