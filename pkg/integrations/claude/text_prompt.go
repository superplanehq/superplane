func (c *TextPrompt) Setup(ctx core.SetupContext) error {
	spec := TextPromptSpec{}
	if err := mapstructure.Decode(ctx.Configuration, &spec); err != nil {
		return fmt.Errorf("failed to decode configuration: %v", err)
	}
	if spec.Model == "" {
		return fmt.Errorf("model is required")
	}

	if spec.Prompt == "" {
		return fmt.Errorf("prompt is required")
	}

	// Validate that configured files exist in the repository
	if len(spec.Files) > 0 {
		if ctx.Files == nil {
			return fmt.Errorf("files configured but file access is not available")
		}
		available, err := ctx.Files.List()
		if err != nil {
			return fmt.Errorf("failed to list repository files: %v", err)
		}
		fileSet := make(map[string]bool, len(available))
		for _, f := range available {
			if norm, err := gitprovider.NormalizePath(f); err == nil {
				fileSet[norm] = true
			}
		}
		for _, f := range spec.Files {
			norm, err := gitprovider.ValidateUserPath(f)
			if err != nil {
				return fmt.Errorf("invalid file path %q: %v", f, err)
			}
			if !fileSet[norm] {
				return fmt.Errorf("file %q not found in app repository", f)
			}
		}
	}

	return nil
}

func (c *TextPrompt) Execute(ctx core.ExecutionContext) error {
	spec := TextPromptSpec{}
	if err := mapstructure.Decode(ctx.Configuration, &spec); err != nil {
		return fmt.Errorf("failed to decode configuration: %v", err)
	}
	if spec.Model == "" {
		return fmt.Errorf("model is required")
	}
	if spec.Prompt == "" {
		return fmt.Errorf("prompt is required")
	}

	if spec.MaxTokens == 0 {
		spec.MaxTokens = 4096
	}

	if spec.MaxTokens < 1 {
		return fmt.Errorf("maxTokens must be at least 1")
	}

	client, err := NewClient(ctx.HTTP, ctx.Integration)
	if err != nil {
		return err
	}

	// Read attached repository files and build the message content. Files are
	// uploaded to the Files API and referenced by file_id; the prompt goes last.
	attachments, err := llmattach.Read(ctx.Files, spec.Files)
	if err != nil {
		return fmt.Errorf("failed to read attachments: %v", err)
	}
	userContent, fileIDs, err := buildUserContent(client, attachments, spec.Prompt)
	if err != nil {
		return err
	}
	defer cleanupFiles(client, fileIDs)

	req := CreateMessageRequest{
		Model:     spec.Model,
		MaxTokens: spec.MaxTokens,
		Messages: []Message{
			{
				Role:    "user",
				Content: userContent,
			},
		},
		Temperature: spec.Temperature,
	}

	if spec.SystemMessage != "" {
		req.System = spec.SystemMessage
	}

	response, err := client.CreateMessage(req)
	if err != nil {
		return err
	}

	text := extractMessageText(response)

	payload := MessagePayload{
		ID:         response.ID,
		Model:      response.Model,
		Text:       text,
		Usage:      &response.Usage,
		StopReason: response.StopReason,
		Response:   response,
	}

	return ctx.ExecutionState.Emit(
		core.DefaultOutputChannel.Name,
		MessagePayloadType,
		[]any{payload},
	)
}

