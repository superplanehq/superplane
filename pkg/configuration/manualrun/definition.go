package manualrun

import (
	"fmt"
	"slices"
)

// ParamType is the data type for a manual-run parameter.
type ParamType string

const (
	ParamTypeString  ParamType = "string"
	ParamTypeNumber  ParamType = "number"
	ParamTypeBoolean ParamType = "boolean"
	ParamTypeSelect  ParamType = "select"
)

// Definition describes a param() leaf discovered in a template payload.
type Definition struct {
	// Path is the dot/bracket path of the leaf in the template
	// (for example, body.name or items[0].id).
	// It is also the key used in runParams at invoke time.
	Path string
	// Type is the declared data type from the param() expression.
	Type ParamType
	// Title is the human-readable label from the param() expression,
	// shown in run forms.
	Title string
	// Default is the value used when runParams omits this path.
	// Nil when no default was declared.
	Default any
	// Required is true when the param() expression sets required:true
	// and no default satisfies the run.
	Required bool
	// Values lists allowed options for select params.
	// Empty for non-select types.
	Values []string
	// Order controls display order in autogenerated run forms only.
	// Omitted order keys parse as 0. ApplyParams ignores this field.
	Order int
}

// NewDefinition builds a validated param() definition at path.
func NewDefinition(
	path string,
	paramType ParamType,
	title string,
	defaultValue any,
	required bool,
	values []string,
	order int,
) (Definition, error) {
	if path == "" {
		return Definition{}, fmt.Errorf("param() path is required")
	}
	if paramType == "" {
		return Definition{}, fmt.Errorf("param() missing type")
	}

	switch paramType {
	case ParamTypeSelect:
		if len(values) == 0 {
			return Definition{}, fmt.Errorf("select param requires values")
		}
		if defaultValue != nil {
			defaultStr, ok := defaultValue.(string)
			if !ok {
				return Definition{}, fmt.Errorf("select default must be a string")
			}
			if !slices.Contains(values, defaultStr) {
				return Definition{}, fmt.Errorf("default %q is not one of the select values", defaultStr)
			}
		}
	case ParamTypeString, ParamTypeNumber, ParamTypeBoolean:
		if len(values) > 0 {
			return Definition{}, fmt.Errorf("values is only valid for select params")
		}
	default:
		return Definition{}, fmt.Errorf("unsupported type %q", paramType)
	}

	return Definition{
		Path:     path,
		Type:     paramType,
		Title:    title,
		Default:  defaultValue,
		Required: required,
		Values:   values,
		Order:    order,
	}, nil
}
