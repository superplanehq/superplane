import * as Sentry from "@sentry/react";

interface SentryWindow extends Window {
  SUPERPLANE_SENTRY_DSN?: string;
  SUPERPLANE_SENTRY_ENVIRONMENT?: string;
}

// Errors that should be dropped before being sent to Sentry. These are
// dominated by noise that cannot be acted on from application code:
//
//   - Minified third-party / vendor code (e.g. bundled React DOM internals,
//     mermaid's GWT-compiled ELK) occasionally raises
//     `ReferenceError: Can't find variable: <minified-name>` on Safari/WebKit,
//     usually triggered by browser extensions injecting scripts into the page.
//     The variable names are auto-generated by the minifier and change every
//     release, so we match the family with a regex instead of a single name.
//   - Browser extensions (autofill, password managers, translation widgets,
//     ad blockers) frequently leak undefined globals into the page.
//   - Browser quirks such as `ResizeObserver` loop notifications, opaque
//     `Script error.` events, and aborted fetches do not represent real
//     application bugs.
export const IGNORED_ERRORS: (string | RegExp)[] = [
  // Vendor / extension noise — short minified identifiers (e.g. "J4", "ABC12",
  // "$a9") almost never come from our own (TypeScript-checked) source code.
  // The bound `{1,4}\d{1,3}` keeps the pattern conservative so that real
  // application identifiers (e.g. `counter1`, `react18`) are not swallowed.
  /Can't find variable: [A-Za-z_$]{1,4}\d{1,3}$/,
  /^[A-Za-z_$]{1,4}\d{1,3} is not defined$/,

  // Common browser-extension globals (Safari autofill, Firefox internals, etc.)
  /Can't find variable: __AutoFillPopupClose__/,
  /Can't find variable: _AutofillCallbackHandler/,
  /Can't find variable: __firefox__/,
  /Can't find variable: __gCrWeb/,

  // Harmless ResizeObserver scheduling notifications.
  /ResizeObserver loop limit exceeded/,
  /ResizeObserver loop completed with undelivered notifications/,

  // Aborted / cancelled network requests during page navigation.
  /^Load failed$/,
  /^Failed to fetch$/,
  /^NetworkError when attempting to fetch resource\.?$/,
  /^TypeError: cancelled$/i,

  // Cross-origin script errors with no useful stack frame.
  /^Script error\.?$/,
  /^Non-Error promise rejection captured/,
];

let dsn: string | undefined;
let environment: string | undefined;

if (typeof window !== "undefined") {
  const sentryWindow = window as SentryWindow;
  dsn = sentryWindow.SUPERPLANE_SENTRY_DSN;
  environment = sentryWindow.SUPERPLANE_SENTRY_ENVIRONMENT;
}

if (dsn) {
  Sentry.init({
    dsn,
    environment,
    ignoreErrors: IGNORED_ERRORS,
    integrations: [
      Sentry.captureConsoleIntegration({
        levels: ["warn", "error"],
      }),
      Sentry.browserApiErrorsIntegration({
        setTimeout: true,
        setInterval: true,
        requestAnimationFrame: true,
        XMLHttpRequest: true,
        eventTarget: true,
      }),
      Sentry.globalHandlersIntegration({
        onerror: true,
        onunhandledrejection: true,
      }),
    ],
  });
}

export { Sentry };
